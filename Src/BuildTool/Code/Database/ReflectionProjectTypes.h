#pragma once


#include <vector>

#include "Core/FileSystem.h"
#include "Core/StringID.h"
#include "Core/Utils.h"

//-------------------------------------------------------------------------

namespace SE::BuildTool
{
    typedef StringID HeaderID;
    typedef StringID ProjectID;

    //-------------------------------------------------------------------------

    struct HeaderInfo
    {
        inline static HeaderID GetHeaderID(std::string &headerFilePath)
        {
			std::string lowercasePath = Utils::String::ToLowerCopy(headerFilePath);
			FileSystem::NormalizePath(lowercasePath);
            return StringID(lowercasePath);
        }

    public:
        inline bool IsInToolsLayer() const
        {
            return Utils::IsFileUnderToolsProject(filePath);
        }

        inline std::string GetAutogeneratedTypeInfoFileName(std::string const &directoryPath) const
        {
		std::string outPath = GetAutogeneratedFileName(directoryPath);
            outPath.push_back('.');
			outPath.push_back('h');
            return outPath;
        }

    private:
        inline std::string GetAutogeneratedFileName(std::string const &directoryPathStr) const
        {
            ENGINE_ASSERT(!directoryPathStr.empty());
			std::string filename = FileSystem::GetFileNameWithoutExtension(filePath);
			std::string filePath = Utils::String::Format("{0}{1}.typeinfo", directoryPathStr, filename);
			FileSystem::NormalizePath(filePath);
            return filePath;
        }

    public:
        HeaderID headerId;
        ProjectID projectID;
		std::string filePath;
        uint64_t timestamp = 0;
        uint64_t checksum = 0;
		std::vector<std::string> fileContents;
    };

    //-------------------------------------------------------------------------

    struct ProjectInfo
    {
        inline static ProjectID GetProjectID(std::string &projectFilePath)
        {
			std::string lowercasePath = Utils::String::ToLowerCopy(projectFilePath);
            return StringID(lowercasePath);
        }

    public:
        ProjectID id;
		std::string name;
		std::string path;
		std::string parentPath;
		std::string exportMacro;
        HeaderID moduleHeaderID;
        uint32_t dependencyCount = 0;
		std::vector<HeaderInfo> headerFiles;
		std::vector<uint32> dirtyHeaders;
		std::vector<ProjectID> dependencies;
        // 命名空间
        mutable std::string moduleClassNameFull;
        // 不带命名空间
        mutable std::string moduleClassName;
        
        bool includeInAutogenerateModuleRegistrationList = true;
        bool isToolsModule = false;
    };

    //-------------------------------------------------------------------------
    struct SolutionInfo
    {
		std::string path;
		std::vector<ProjectInfo> projects;
		std::vector<std::string> excludedProjects;
    };
}
