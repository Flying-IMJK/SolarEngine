#pragma once

#include "../ReflectorSettingsAndUtils.h"
#include "Core/Types/Strings/StringID.h"
#include "Core/Platform/FileSystem.h"

#include <vector>

//-------------------------------------------------------------------------

namespace SE::ReflectTool
{
    typedef StringID HeaderID;
    typedef StringID ProjectID;

    //-------------------------------------------------------------------------

    struct HeaderInfo
    {
    	static HeaderID GetHeaderID(String &headerFilePath)
        {
			String lowercasePath = headerFilePath.ToLower();
			FileSystem::NormalizePath(lowercasePath);
            return StringID(lowercasePath);
        }

    public:
    	bool IsInToolsLayer() const
        {
            return Utils::IsFileUnderToolsProject(filePath.ToString());
        }

    	String GetAutogeneratedTypeInfoFileName(String const &directoryPath) const
        {
			String outPath = GetAutogeneratedFileName(directoryPath);
            outPath.Append('.');
			outPath.Append('h');
            return outPath;
        }

    private:
    	String GetAutogeneratedFileName(String const &directoryPathStr) const
        {
            ENGINE_ASSERT(!directoryPathStr.IsEmpty());
			String filename = FileSystem::GetFileNameWithoutExtension(filePath.ToString());
			String filePath = String::Format(SE_TEXT("{0}{1}.typeinfo"), directoryPathStr.Get(), filename);
			FileSystem::NormalizePath(filePath);
            return filePath;
        }

    public:
        HeaderID headerId;
        ProjectID projectID;
		StringAnsi filePath;
        uint64_t timestamp = 0;
        uint64_t checksum = 0;
		List<StringAnsi> fileContents;
    };

    //-------------------------------------------------------------------------

    struct ProjectInfo
    {
    	static ProjectID GetProjectID(String &projectFilePath)
        {
			String lowercasePath = projectFilePath.ToLower();
            return StringID(lowercasePath);
        }
    public:
        ProjectID id;
		String name;
		String path;
		String parentPath;
		String exportMacro;
        // HeaderID moduleHeaderID;
        uint32 dependencyCount = 0;
		std::vector<HeaderInfo> headerFiles;
		std::vector<uint32> dirtyHeaders;
		std::vector<ProjectID> dependencies;
        // 命名空间
        mutable String moduleClassNameFull;
        // 不带命名空间
        mutable String moduleClassName;
        
        bool hasBindingAPI = true;
        bool isToolsModule = false;
    };

    //-------------------------------------------------------------------------
    struct SolutionInfo
    {
		String path;
		List<ProjectInfo> projects;
		List<String> excludedProjects;
    };
}